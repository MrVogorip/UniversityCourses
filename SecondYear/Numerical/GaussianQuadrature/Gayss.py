def Gayss(a,b,n):
    T = [[0,0,0,0,0], 
         [-0.774597, 0, 0.774597, 0, 0], 
         [0, 0, 0, 0, 0],
         [0.861136, -0.339981, 0.339981, 0.861136, 0], 
         [-0.906180, -0.538469, 0, 0.538469, 0.906180]]
    A = [[2, 0, 0, 0, 0],
         [0, 0, 0, 0, 0], 
         [5/9, 8/9, 5/9, 0, 0], 
         [0.347855, 0.652145, 0.652145, 0.347855, 0], 
         [0.236927, 0.478629, 0.568889, 0.478629, 0.236927]]
